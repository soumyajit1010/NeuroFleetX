package NeuroFleetX.JavaExercises.Operator_Programs;// Program2.java
// Demonstrates relational operators in Java

public class Program2 {
    public static void main(String[] args) {
        int x = 8, y = 12;

        System.out.println("x == y: " + (x == y));
        System.out.println("x != y: " + (x != y));
        System.out.println("x > y: " + (x > y));
        System.out.println("x < y: " + (x < y));
        System.out.println("x >= y: " + (x >= y));
        System.out.println("x <= y: " + (x <= y));
    }
}
